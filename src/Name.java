public class Name {
    String state;
    char sex;
    int birthYear;
    String name;
    int count;
    
    public Name(String state, char sex, int birthYear, String name, int count) {
        this.state = state;
        this.sex = sex;
        this.birthYear = birthYear;
        this.name = name;
        this.count = count;
    }
}
